// Write a program to check number is even or odd.

#include <stdio.h>

void main() {

    int num1 = 44;

    if (num1 % 2 == 0) {

        printf("num1 %d is even\n", num1);
    }
    else {
        printf("num1 %d is odd\n", num1);
    }


    int num2 = 13;

    if (num2 % 2 == 0) {

        printf("num2 %d is even\n", num2);
    }
    else {
        printf("num2 %d is odd\n", num2);
    }


}