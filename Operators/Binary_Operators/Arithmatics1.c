//Arithmatics Operators
/*
	+ : Addition
	- : Substarction
	* : Multiplication
	/ : Division
	% : Modulus
*/


#include <stdio.h>

void main() {
	
	int x = 20;
	int y = 10;

	printf("x + y = %d\n", x+y);        //30
	printf("x - y = %d\n", x-y);		//10
	printf("x * y = %d\n", x*y);		//200
	printf("x / y = %d\n", x/y);		//2
	printf("x % y = %d\n", x%y);		//0

}
