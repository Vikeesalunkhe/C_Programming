/*
 int %d
 float %f
 char %c
 double %lf
 long %ld
 */

#include <stdio.h>

void main(){
	int value = 25;
	float height = 123.4;
	char alphabet = 'A';
       	double PI = 3.1434939;
	long data = 23493894;

	printf("value is : %d\n", value);
	printf("height is : %f\n", height);
	printf("alphabet is : %c\n", alphabet);
	printf("PI is : %lf\n", PI);
	printf("data is : %ld\n", data);	
}
